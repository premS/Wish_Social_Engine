package databeans;

public class history {
	String userName;
	String year;
	String success;
	String failure;
	
	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getYear() {
		return year;
	}

	public void setYear(String userName) {
		this.year = userName;
	}
	public String getSuccess() {
		return success;
	}

	public void setSuccess(String userName) {
		this.success = userName;
	}
	public String getFailure() {
		return failure;
	}

	public void setFailure(String userName) {
		this.failure = userName;
	}


}
